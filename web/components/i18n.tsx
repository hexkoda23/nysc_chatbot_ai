type Ctx = {
  lang: Lang
  setLang: (l: Lang) => void
  t: (k: keyof Dict) => string
}

const LanguageContext = createContext<Ctx | null>(null)

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLang] = useState<Lang>('en')
  useEffect(() => {
    try {
      const saved = localStorage.getItem('nysc_lang')
      if (saved && ['en', 'yo', 'ha', 'ig'].includes(saved)) setLang(saved as Lang)
    } catch { }
  }, [])
  const value = useMemo<Ctx>(() => {
    const bundle = translations[lang]
    const t = (k: string) => bundle[k] ?? translations.en[k] ?? k
    const setL = (l: Lang) => {
      setLang(l)
      try {
        localStorage.setItem('nysc_lang', l)
      } catch { }
    }
    return { lang, setLang: setL, t }
  }, [lang])
  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useI18n() {
  const ctx = useContext(LanguageContext)
  if (!ctx) throw new Error('LanguageContext missing')
  return ctx
}
